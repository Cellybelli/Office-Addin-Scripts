/**
 * @customfunction
 * @returns {boolean}
 */
function returnsBoolean() {
  return true;
}

/**
 * @customfunction
 * @returns {number}
 */
function returnsNumber() {
  return 5;
}

/**
 * @customfunction
 * @returns {string}
 */
function returnsString() {
  return "abc";
}

/**
 * @customfunction
 * @returns {object}
 */
function returnsObject() {
  return {};
}

/**
 * @customfunction
 * @returns {string[]}
 */
function returnsArray() {
  return ["abc"];
}

/**
 * @customfunction
 * @returns {any[][]}
 */
function returnsMatrixAny() {
  return [[1, "string"]];
}

/**
 * @customfunction
 * @returns {boolean[][]}
 */
function returnsMatrixBoolean() {
  return [[true]];
}

/**
 * @customfunction
 * @returns {number[][]}
 */
function returnsMatrixNumber() {
  return [[5]];
}

/**
 * @customfunction
 * @returns {string[][]}
 */
function returnsMatrixString() {
  return [["abc"]];
}
